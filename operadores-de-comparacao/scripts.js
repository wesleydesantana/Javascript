//Igualdade

var A = 3;
var B = 6;

//Mesmo Valor
console.log(A == 3);
console.log(B == 3);

//Diferente
console.log(A != 3);
console.log(A != B);


//Identico
console.log(A === 3);
console.log(A === "3");

//Retorna true se o tipo e o valor forem diferentes

console.log(A !== 3);
console.log(A !== "3");

//Maior quê 

console.log(A>B);
console.log(A<B);

//Maior igual quê 

console.log(A>=3);
console.log(A>=7);

//Menor igual quê

console.log(A<=3);
console.log(A<=7);
